/**
 * Clase que representa un autor en la persistencia y permite su serializaci√≥n
 *
 * @author ISIS2603
 */
@Entity
public class ClienteEntity implements Serializable {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;
    private String name;
    private String city;
    private String address;
    @Temporal(TemporalType.DATE)
    private Date aniocreacion;

    /**
     * Devuelve el nombre del autor.
     *
     * @return the empresa
     */
    public String getName() {
        return name;
    }

    /**
     * Modifica el nombre del autor.
     *
     * @param empresa the empresa to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the city
     */
    public String getCity() {
        return city;
    }

    /**
     * @param city the city to set
     */
    public void setCity(String city) {
        this.city = city;
    }

    /**
     * @return the address
     */
    public String getAddress() {
        return address;
    }

    /**
     * @param address the address to set
     */
    public void setAddress(String address) {
        this.address = address;
    }

    /**
     * @return the id
     */
    public Long getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(Long id) {
        this.id = id;
    }

    public Date getAniocreacion() {
        return aniocreacion;
    }

    public void setAniocreacion(Date aniocreacion) {
        this.aniocreacion = aniocreacion;
    }

}
